    private void ChannelDeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChannelDeleteButtonActionPerformed
        // TODO add your handling code here:
        int row = ChannelTable.getSelectedRow();
        if(row >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete " +(Channel)ChannelTable.getValueAt(row, 0) + " channel?","Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                business.cl.DeleteChannel((Channel)ChannelTable.getValueAt(row, 0));
                Populate();
            }
        }else{
            JOptionPane.showMessageDialog(null,"Select a row!");
        }
    }//GEN-LAST:event_ChannelDeleteButtonActionPerformed

