    private void Populate(){
        DefaultTableModel dtm = (DefaultTableModel) ChannelTable.getModel();
        dtm.setRowCount(0);
        for(Channel channel : business.cl.getChannelList()){
            Object[] row = new Object[1];
            row[0] = channel;
            dtm.addRow(row);
        }
    }
    
    private void ChannelSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChannelSearchButtonActionPerformed
        // TODO add your handling code here:
        String search = ChannelSearchText.getText();
        if(search != null){
            Channel c = business.cl.searchChannel(search);
            if(c != null){
                DefaultTableModel dtm = (DefaultTableModel) ChannelTable.getModel();
                dtm.setRowCount(0);
                Object[] row = new Object[1];
                row[0] = c;
                dtm.addRow(row);
            }
        }
    }//GEN-LAST:event_ChannelSearchButtonActionPerformed

